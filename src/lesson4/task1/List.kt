    var result = mutableListOf<Int>();
    var newN = n
    var element:Int;
    var x:Int = 10;
    var bit: Int = 1;
    do {
        element = if (newN % (x * 10) in 10..19){
            (newN % x) + 10;
            //newN -= 10;
        } else newN % x;
        result.add(element);
        newN -= element;
        bit++;
        x *= 10;
        if (bit == 4) {
