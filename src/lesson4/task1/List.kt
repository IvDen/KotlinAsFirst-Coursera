fun russian(n: Int): String {
    var list = mutableListOf<String>();
    var splDigit:List<Int> = splitDigit(n);
    var bit:Int = 1;
    splDigit.forEach {
        if (it != 0) {
            list.add(toNumeral(it,bit))
        } else if (bit == 4) list.add(toNumeral(it,bit))
        bit++;


    }


    return  list.asReversed().joinToString(" ");
}

